function u = Unit( v )
%#eml
%-------------------------------------------------------------------------------
%   Unitize vectors by column.
%-------------------------------------------------------------------------------
%   Form:
%   u = Unit( v )
%-------------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   v            (:,n)     Vectors
%
%   -------
%   Outputs
%   -------
%   u            (:,n)     Unit vectors
%
%-------------------------------------------------------------------------------

%-------------------------------------------------------------------------------
%	 Copyright 1994 Princeton Satellite Systems, Inc. All rights reserved.
%-------------------------------------------------------------------------------


  u= (v./sqrt(sum(v.^2)));



