function dy=fei(~,y)
a0 = 42378137;

u = 3.986005e+14;

n = sqrt(u/a0^3);
dy = [y(4);
     y(5);
     y(6);
     2*n*y(5) + n^2*y(1)-u * (a0+y(1)) / ((a0+y(1))^2 + y(2)^2 + y(3)^2)^1.5 + u/(a0^2);
     -2*n*y(4) + n^2*y(2)-u * y(2) / ((a0 + y(1))^2 + y(2)^2 + y(3)^2)^1.5;
     -u*y(3) / ((a0+y(1))^2 + y(2)^2 + y(3)^2)^1.5];

end